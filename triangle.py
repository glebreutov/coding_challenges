import itertools
import sys
from typing import List


class Solution:

    def minimumTotal(self, triangle: List[List[int]]) -> int:
        prev_level = triangle[0]
        for level in triangle[1:]:
            c_level = []
            for idx, val in enumerate(level):
                if idx - 1 >= 0:
                    a = prev_level[idx - 1] + val
                else:
                    a = sys.maxsize

                if len(prev_level) > idx:
                    b = prev_level[idx] + val
                else:
                    b = sys.maxsize
                c_level.append(min(a, b))

            prev_level = c_level

        return min(prev_level)


def test_case1():
    triangle = [[2], [3, 4], [6, 5, 7], [4, 1, 8, 3]]
    exp = 11

    assert Solution().minimumTotal(triangle) == exp


def test_case2():
    triangle = [[-10]]
    exp = -10
    assert Solution().minimumTotal(triangle) == exp


def test_case3():
    triangle = [
        [2],
        [3, 4],
        [6, 5, 9],
        [4, 4, 8, 0]]
    exp = 14
    assert Solution().minimumTotal(triangle) == exp


